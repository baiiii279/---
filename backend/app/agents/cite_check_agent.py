from app.agents.base import BaseAgent, SharedContext
from crewai import Task

CITE_CHECK_PROMPT = """你是一位严谨的学术审计员，专门负责论文引用规范审查。请对以下论文进行逐项检查。

请先不要输出报告。按照以下思维链逐步检查，每一步完成后再进入下一步。

**思考链：**

**第1步 - 提取引用信息**：先扫描全文——
- 找出所有形如 [N] 的引用标记，记录它们的位置和序号
- 列出参考文献列表中的所有条目
- 在脑中建立"引用标记 ↔ 文献条目"的对应表

**第2步 - 逐项检查（按以下顺序依次进行）**：

*检查A - 引用缺失*：逐段阅读正文——
- 统计数据、实证结果 → 是否有引用？
- 他人观点、理论陈述 → 是否有引用？
- "研究表明"、"有学者指出"这类表述 → 后面是否跟了具体引用？
- 找出所有"应该有引用但没引用"的位置

*检查B - 格式一致性*：检查所有引用标记——
- 格式是否统一？有没有混用 [N] 和 (N) 的情况？
- 连续引用格式是否一致？
- 引用标记是否放在恰当位置？

*检查C - 引用-文献对应*：核对——
- 正文中出现的每个 [N] 在文献列表中是否存在？
- 文献列表中的每个条目是否都被正文引用过？
- 引用编号是否连续？有没有跳号？

*检查D - 引用充分性*：评估——
- 核心论点是否有2-3篇以上的文献支持？
- 是否存在连续5句以上无引用的段落？
- 引用来源是否过于集中？

**第3步 - 生成建议**：基于以上检查——
- 对每个问题，判断其严重程度（高/中/低）
- 对每个问题，给出具体的修正建议
- 总结整体的引用质量

**第4步 - 自检**：输出前确认——
- [ ] 是否覆盖了所有检查项？
- [ ] 每个问题是否有具体的位置描述和修正建议？
- [ ] 严重程度判断是否合理？

思考完成，现在输出结构化检查报告。

## 输出格式

以结构化 Markdown 报告输出：

## 引用检查报告

### ✅ 通过项
- [检查项]: 通过/不通过

### ❌ 问题列表
| 问题类型 | 位置 | 具体描述 | 严重程度 | 修正建议 |
|---------|------|---------|---------|---------|
| 引用缺失 | 第X章第X段 | ... | 高/中/低 | ... |

### 统计
- 正文引用标记总数: N
- 文献列表条目数: M
- 格式问题数: N
- 引用缺失数: N

### 优化建议
- ...

严重程度定义：
- **高**：引用缺失或引用错误 → 必须修正，否则构成学术不端
- **中**：引用格式不规范或引用不够充分 → 建议修正
- **低**：引用布局或编号顺序可优化 → 仅供参考

论文内容：
{content}

参考文献列表：
{references}"""


class CiteCheckAgent(BaseAgent):
    def __init__(self):
        super().__init__(
            role="学术审计员",
            goal="逐项检查论文引用的完整性、准确性和格式规范性，输出结构化检查报告",
            backstory="""你是一位严谨的学术审计员，在多家学术期刊担任过引用规范审查员。
你对学术诚信有近乎偏执的坚持——每一处引用都经得起推敲是你的工作标准。
你深知引用规范不仅是格式问题，更是学术诚信的核心体现。
你曾经发现过引用不存在文献的严重学术不端案例，也帮助过无数研究者完善他们的引用规范。
你的检查报告以"不放过任何一个漏洞，但每个结论都有依据"著称。""",
        )

    def run(self, context: SharedContext) -> str:
        refs_text = "\n".join(
            f"[{i+1}] {r['title']}. {r.get('authors', '未知')}. {r.get('source', '')}. {r.get('year', '')}"
            for i, r in enumerate(context.references)
        )
        content = context.polished_content or context.content
        task = Task(
            description=CITE_CHECK_PROMPT.format(content=content, references=refs_text),
            agent=self._get_or_create_agent(),
            expected_output="""结构化引用检查报告，包含：
1. 引用缺失检查结果
2. 引用格式一致性检查结果
3. 引用-文献对应检查结果
4. 引用充分性评估
5. 改进建议和严重程度分级""",
        )
        return self._execute_task(task)
