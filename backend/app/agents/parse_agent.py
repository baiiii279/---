from app.agents.base import BaseAgent, SharedContext
from crewai import Task

PARSE_PROMPT = """你是一位顶尖的文献分析师。你的任务是对用户提供的参考文献进行深度解析，提取可用于论文写作的核心信息。

请按以下步骤进行分析：

## 第一步：整体评估
快速浏览所有文献，判断它们的研究主题一致性、方法论分布和论证链条。
- 这些文献属于同一研究方向吗？还是存在多个子方向？
- 主要采用了哪些研究方法（实证/理论/综述/混合）？
- 文献之间的引用关系和观点传承是怎样的？

## 第二步：逐篇深度解析

对每篇文献，输出以下内容：

### [文献序号] 文献标题
**研究问题**：该文献试图解决什么核心问题？研究动机是什么？
**理论基础**：基于哪些理论或框架？与之前工作的关系？
**方法设计**：采用了什么研究方法？数据来源？实验设计？样本规模？
**核心发现**：最关键的3-5个研究发现或结论（用数据说话，避免空泛描述）
**局限性**：作者承认的局限，或你能识别的方法论缺陷（这为论文的"相关工作"和"未来工作"部分提供素材）
**与论文主题的关联度**：高/中/低 — 并说明为什么
**建议引用章节**：引言/相关工作/方法论/实验/讨论 — 说明理由

## 第三步：综合分析
在逐篇分析完成后，输出：
1. **研究现状总结**：用2-3句话概括该领域当前的研究状态
2. **研究空白**：现有文献中尚未充分探讨的问题（这是你论文的切入点）
3. **文献间的对话**：哪些文献互相支持？哪些存在观点冲突？
4. **方法倾向**：该领域的主流方法是什么？有没有值得尝试的新方法？

## 输出格式要求
- 每篇文献之间用 --- 分隔
- 分析必须基于文献实际内容，严禁编造
- 如果某篇文献的摘要信息不足，标注"信息不足，需进一步阅读全文"而不是编造内容
- 所有内容使用中文

文献列表：
{references}

现在开始分析。"""


class ParseAgent(BaseAgent):
    def __init__(self):
        super().__init__(
            role="文献分析师",
            goal="深度解析用户提供的文献，提取研究问题、方法论、核心发现、局限性，并分析文献间的关联和对话",
            backstory="""你是一位顶级的学术文献分析师，拥有20年跨学科研究经验。
你的核心能力是穿透文献的表面表述，精准识别其研究设计、方法论贡献和逻辑链条。
你能快速建立多篇文献之间的关联网络，发现研究空白和争议点。
你深知学术写作中"站在巨人肩膀上"的真正含义——不是堆砌引用，而是精准定位每篇文献在知识图谱中的位置。""",
        )

    def run(self, context: SharedContext) -> str:
        refs_text = "\n\n".join(
            f"标题: {r['title']}\n作者: {r.get('authors', '未知')}\n摘要: {r.get('abstract', '无')}"
            for i, r in enumerate(context.references)
        )
        task = Task(
            description=PARSE_PROMPT.format(references=refs_text),
            agent=self._get_or_create_agent(),
            expected_output="""结构化文献分析报告，包含：
1. 整体研究状态评估
2. 每篇文献的深度解析（研究问题、方法、发现、局限、关联度）
3. 跨文献综合分析（研究空白、观点对话、方法倾向）""",
        )
        return self._execute_task(task)
